    private void btn_PasschangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_PasschangeActionPerformed
        String New_Pass = txt_Newpass.getText().toString();
         String Confirm_Pass = txt_ConfirmPass.getText().toString();
         
        boolean valid = objd.validatePass(New_Pass, Confirm_Pass);
        if (valid){
           objd.setPassword(txt_ConfirmPass.getText());
           objd.setUsername(txt_Username1.getText());
        objd.Updates();
        JOptionPane.showMessageDialog(null, "Password changed");
        }
         
    }//GEN-LAST:event_btn_PasschangeActionPerformed

