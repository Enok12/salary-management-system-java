    private void btn_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SearchActionPerformed
      try  
      {
          objd.setUsername(txt_Username1.getText());
        ResultSet rs=objd.SearchUser();
          
          
          
          if (rs.next())
          {
          jusertype.setText(rs.getString("Usertype"));
          jFName.setText(rs.getString("First_Name"));
          jLName.setText(rs.getString("Last_Name"));
          JOptionPane.showMessageDialog(null, "User found");
          }else{
          JOptionPane.showMessageDialog(null, "User not found");
          }
         
          }
      catch (Exception ex)
       {
           JOptionPane.showMessageDialog(null,"Error searchinig data"+ex);
       } 
    }//GEN-LAST:event_btn_SearchActionPerformed

